static uint8_t anim_frame = 0;
static uint32_t anim_timer = 0;

static void oled_render_anim_master(void) {
    oled_set_cursor(0, 0);

    if (timer_elapsed32(anim_timer) > ANIM_TIMEOUT) {
        anim_timer = timer_read32();
        anim_frame = (anim_frame + 1) % ANIM_FRAMES;

        switch (anim_frame) {
            case 1:
                oled_write_raw_P(pika_frame_01, sizeof(pika_frame_01));
                break;
            case 2:
                oled_write_raw_P(pika_frame_02, sizeof(pika_frame_02));
                break;
            case 3:
                oled_write_raw_P(pika_frame_03, sizeof(pika_frame_03));
                break;
            default:
                oled_write_raw_P(pika_frame_04, sizeof(pika_frame_04));
                break;

        }
    }

    // oled_render_wpm();
}

static void oled_render_anim_slave(void) {
    oled_render_anim_master();
}
